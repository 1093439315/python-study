# 比较（判断）运算符
'''
>
<
>=
<=
==
!=
not ==

a = 100
b = -50
print('a > b', a > b)
print('a < b', a < b)
print('a >= b', a >= b)
print('a <= b', a <= b)
print('a == b', a == b)
print('a != b', a != b)
print('not a == b', not a == b)
'''
# 逻辑运算符
'''
and 逻辑与
or  逻辑或
not 逻辑非
'''

a = 100
b = -50
print('a > 0 and a > b', a > 0 and a > b)
print('b > 0 or b > a', b > 0 or b > a)
print('not a > b', not a > b)
